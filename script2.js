// Arithmetic Operators//
let num1=8;
let num2=3;

//Addition
console.log(num1+num2 );

//Subtraction
console.log(num1-num2 );

//Multiplication
console.log(num1*num2);

//Division
console.log(num1/num2);

//Modulus
console.log(num1%num2);

//logical operator//

let x=true;
let y=false;

//logical AND

console.log(x&&y);

//logical OR
console.log(x||y);

//logical NOT
console.log(!x);

//Comparision operator//

let a=50;
let b=30;

//equal to
console.log(a==b);

//Strict equal to
console.log(a===b);

//Not equal to
console.log(a!=b);

//Strict not equal to
console.log(a!==b);

//Less than
console.log(a<b);

//Grater than
console.log(a>b);

//Less than or Equal to
console.log(a<=b);

//Greater than or Equal to
console.log(a>=b);


